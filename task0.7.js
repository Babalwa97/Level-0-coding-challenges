function celsiusToFahrenheit() {
  var temp = 70;
  var convert = temp * (9 / 5) + 32;
  var answer = temp + "\xB0C is " + convert + " \xB0F."; {
  return answer;
 }
}


function fahrenheitToCelsius() { 
  var temperature = 80;
  var convert = (temperature - 32) * (5 / 9);
  var answer = temperature + "\xB0F is " + convert + " \xB0C."; {
  return answer;
 }
}
